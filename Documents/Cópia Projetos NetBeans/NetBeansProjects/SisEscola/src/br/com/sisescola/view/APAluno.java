    protected void atualizarGrade() {
        try {
            ResultSet rs = new AlunoDAO().carregarGrade();
            tbDados.setModel(new ModeloGrade(rs, new String[]{"Código", "Nome", "Turno", "Turma"}));
            tbDados.getColumnModel().getColumn(0).setMaxWidth(50);
        } catch (Exception e) {
            showMessageDialog(this, e.getMessage(), "Erro", ERROR_MESSAGE);
        }
    }

    public void popularTurma() {
        try {
            Statement stm = ce.getConexao().createStatement();
            ResultSet rs = stm.executeQuery("SELECT * FROM TURMA ORDER BY NOME");

            rs.first();
            coTurma.removeAllItems();
            do {
                coTurma.addItem(rs.getString("NOME"));
            } while (rs.next());
        } catch (SQLException ex) {
            showMessageDialog(this, "Erro ao preencher turma!", "Erro", ERROR_MESSAGE);
        }
    }

    class MouseHandler extends MouseAdapter {

        public void mouseReleased(MouseEvent e) {
            if (e.getButton() != MouseEvent.BUTTON1) {
                return;
            }
            JTable tb = (JTable) e.getSource();
            int lin = tb.getSelectionModel().getMinSelectionIndex();
            String codigo = tb.getModel().getValueAt(lin, 0).toString();

            try {
                carregarRegistro(codigo);
                tpAbas.setSelectedComponent(pnManutencao);
                tfNome.requestFocus();
            } catch (Exception ex) {
                showMessageDialog(APAluno.this, ex.getMessage(), "Erro", ERROR_MESSAGE);
            }
        }
    }


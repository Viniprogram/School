        a.setCodalu(ftCodigo.getText());
        a.setNome(tfNome.getText());
        if (rbMasculino.isSelected()) {
            sexo = "MASCULINO";
        } else if (rbFeminino.isSelected()) {
            sexo = "FEMININO";
        }

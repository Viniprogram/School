    private void tpAbasStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tpAbasStateChanged
        // TODO add your handling code here:
        if (tpAbas.getSelectedIndex() != 0) {
            return;
        }
        for (int i = 0; i < pnManutencao.getComponentCount(); i++) {
            if (pnManutencao.getComponent(i) instanceof JTextComponent) {
                ((JTextComponent) pnManutencao.getComponent(i)).setText("");
            }
            if (pnManutencao.getComponent(i) instanceof JComboBox) {
                ((JComboBox) pnManutencao.getComponent(i)).setSelectedIndex(0);
            }
        }
    }//GEN-LAST:event_tpAbasStateChanged


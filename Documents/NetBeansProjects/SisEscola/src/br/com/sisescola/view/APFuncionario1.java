    private void tpAbasStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tpAbasStateChanged
        // TODO add your handling code here:
        if(tpAbas.getSelectedIndex() != 0)return;
        for(int i = 0; i < pnManutencao.getComponentCount(); i++){
            if(pnManutencao.getComponent(i) instanceof JTextComponent)
                ((JTextComponent)pnManutencao.getComponent(i)).setText("");
            if(pnManutencao.getComponent(i) instanceof JComboBox)
                ((JComboBox)pnManutencao.getComponent(i)).setSelectedIndex(0);
        }
    }//GEN-LAST:event_tpAbasStateChanged

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if(evt.getSource() == btExcluir){
            int opcao = showOptionDialog(this, "Tem certeza que deseja excluir esse funcionário?", "Confirme", 0,
                    QUESTION_MESSAGE, null, new String[]{"Sim", "Não"}, "Não");
            if (opcao == -1) {
                return;
            } else if (opcao == 0) {
                excluir();
            } else if (opcao == 1) {
                return;
            }            
        }
    }//GEN-LAST:event_btExcluirActionPerformed
  


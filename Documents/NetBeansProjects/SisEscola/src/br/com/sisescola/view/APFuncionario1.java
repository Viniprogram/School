    public void popularDpto() {
        try {
            Statement stm = ce.getConexao().createStatement();
            ResultSet rs = stm.executeQuery("SELECT * FROM DEPTO ORDER BY NOME");

            rs.first();
            coDpto.removeAllItems();
            do {
                coDpto.addItem(rs.getString("NOME"));
            } while (rs.next());
        } catch (SQLException ex) {
            showMessageDialog(this, "Erro ao preencher departamento!", "Erro", ERROR_MESSAGE);
        }
    }      
    
    class MouseHandler extends MouseAdapter{
        public void mouseReleased(MouseEvent e){
            if(e.getButton() != MouseEvent.BUTTON1) return;
            JTable tb = (JTable)e.getSource();
            int lin = tb.getSelectionModel().getMinSelectionIndex();
            String codigo = tb.getModel().getValueAt(lin, 0).toString();

            try {
                carregarRegistro(codigo);
                tpAbas.setSelectedComponent(pnManutencao);
                tfNome.requestFocus();
            } catch (Exception ex) {
                showMessageDialog(APFuncionario1.this, ex.getMessage(),"Erro", ERROR_MESSAGE);
            }
        }
    }
    
